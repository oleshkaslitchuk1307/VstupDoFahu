// === Вкладки ===
document.querySelectorAll(".robot-container").forEach(container => {
  const id = container.id;
  const contentBox = container.querySelector(".tab-content");
  const buttons = container.querySelectorAll(".tab");

  buttons.forEach(btn => {
    btn.addEventListener("click", () => {
      buttons.forEach(b => b.classList.remove("active"));
      btn.classList.add("active");
      const tab = btn.dataset.tab;
      contentBox.innerHTML = `<p>${robotData[id][tab]}</p>`;

          if (tab === "video") {
      window.open(robotData[id].video, "_blank"); 
      contentBox.innerHTML = ""; 
      return;

    }});
  });
});

// === Анімація появи при прокручуванні ===
const observer = new IntersectionObserver(
  entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        observer.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.2 }
);

// Спостерігаємо за всіма роботами
document.querySelectorAll(".robot-container").forEach(container => {
  observer.observe(container);
});


